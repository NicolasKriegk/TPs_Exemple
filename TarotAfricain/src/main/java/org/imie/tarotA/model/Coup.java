package org.imie.tarotA.model;

public class Coup {
	
	private Joueur jouer;
	private Carte carte;
	/**
	 * @return the jouer
	 */
	public Joueur getJouer() {
		return jouer;
	}
	/**
	 * @param jouer the jouer to set
	 */
	public void setJouer(Joueur jouer) {
		this.jouer = jouer;
	}
	/**
	 * @return the carte
	 */
	public Carte getCarte() {
		return carte;
	}
	/**
	 * @param carte the carte to set
	 */
	public void setCarte(Carte carte) {
		this.carte = carte;
	}
	
	
	
	

}
