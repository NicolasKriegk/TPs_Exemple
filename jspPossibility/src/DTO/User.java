public class User {
	private String user;
	private String Password;
	public String getUser() {
		return user;
	}
	public void setLogin(String user) {
		this.user = user;
	}
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		Password = password;
	}
	

}
